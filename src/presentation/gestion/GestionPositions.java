    private void listPositionsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listPositionsMouseClicked
        id = Integer.parseInt(model.getValueAt(listPositions.getSelectedRow(), 0).toString());
        boolean verification = false;
        GPSTracker g = gs.findBySimNumber(model.getValueAt(listPositions.getSelectedRow(), 1).toString());
        
        for(VehiculeGPSTracker vg : vgs.findByTrackerId(g.getId())){    
            if(vg.getDateFin() != null)
                verification = true;
            else
                verification = false;
        }
        
        if(verification){
            JOptionPane.showMessageDialog(this, "Impossible de modifier ou supprimer cette position.");
            id = 0;
        }
        else{
            dateTxt.setDate((Date) model.getValueAt(listPositions.getSelectedRow(), 2));
            longitudeTxt.setText(model.getValueAt(listPositions.getSelectedRow(), 3).toString());
            latitudeTxt.setText(model.getValueAt(listPositions.getSelectedRow(), 4).toString());
        }
    }//GEN-LAST:event_listPositionsMouseClicked

    private void deleteBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteBtnMouseClicked
        if(id != 0){
            int choix = JOptionPane.showConfirmDialog(this, "Vous étes sur?");
            if(choix == 0){
                if(ps.delete(ps.findById(id))){
                    longitudeTxt.setText("Longitude");
                    latitudeTxt.setText("Latitude");
                    id = 0;
                    load();
                }else{
                    JOptionPane.showMessageDialog(this, "Impossible de supprimer cette position.");
                    longitudeTxt.setText("");
                    latitudeTxt.setText("");
                }
            }
        }else
            JOptionPane.showMessageDialog(this, "Aucun position choisi!!!");
    }//GEN-LAST:event_deleteBtnMouseClicked

    private void addBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addBtnMouseClicked
        GPSTracker tracker = gs.findBySimNumber(trackerTxt.getSelectedItem().toString()) ;
        Date date = dateTxt.getDate();
        String longitude = longitudeTxt.getText();
        String latitude = latitudeTxt.getText();
        if(!(date.equals("") || longitude.equals("") || latitude.equals(""))){
            if(ps.create(new Position(Double.parseDouble(latitude), Double.parseDouble(longitude), date, tracker)))
                load();
            else
                JOptionPane.showMessageDialog(this, "Date choisie incorrect.");
        }else
            JOptionPane.showMessageDialog(this, "Tous les champs sont obligatoires.");
    }//GEN-LAST:event_addBtnMouseClicked

    private void updateBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateBtnMouseClicked
        if(id != 0){
            int choix = JOptionPane.showConfirmDialog(this, "Vous étes sur?");
            GPSTracker tracker = gs.findBySimNumber(trackerTxt.getSelectedItem().toString()) ;
            Date date = dateTxt.getDate();
            String longitude = longitudeTxt.getText();
            String latitude = latitudeTxt.getText();
            Position position = ps.findById(id);
            position.setDate(date);
            position.setLongitude(Double.parseDouble(longitude));
            position.setLatitude(Double.parseDouble(latitude));
            position.setGpsTracker(tracker);
            if(choix == 0){
                if(ps.update(position)){
                    longitudeTxt.setText("");
                    latitudeTxt.setText("");
                    id = 0;
                    load();
                }
            }
        }else
            JOptionPane.showMessageDialog(this, "Veuillez choisir une position.");
    }//GEN-LAST:event_updateBtnMouseClicked

