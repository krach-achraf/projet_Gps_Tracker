    private void load() {
        model.setRowCount(0);
        for(Position p : ps.findAll()){
            model.addRow(new Object []{
                p.getId(),
                p.getGpsTracker().getSimNumber(),
                p.getDate(),
                p.getLongitude(),
                p.getLatitude()
            });
        }    
    }
    
    private void loadTrackers(){
        for(GPSTracker g : vgs.findTrackersNull())
            trackerTxt.addItem(g.getSimNumber());      
    }
    
