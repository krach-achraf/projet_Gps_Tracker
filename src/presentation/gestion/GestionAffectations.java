    private void load(){
        model.setRowCount(0);
        for(VehiculeGPSTracker vg : vgs.findAll()){
            
            if(vg.getDateFin() == null)
               model.addRow(new Object []{
                vg.getId(),
                vg.getGpsTracker().getSimNumber(),
                vg.getVehicule().getMatricule(),               
                vg.getDateDebut(),
                "Affecté"
            }); 
            
            else
                model.addRow(new Object []{
                vg.getId(),
                vg.getGpsTracker().getSimNumber(),
                vg.getVehicule().getMatricule(),               
                vg.getDateDebut(),
                vg.getDateFin()
            }); 
               
        }
    }
    
    private void loadTrackers(){
        trackerTxt.removeAllItems();
        for(GPSTracker g : vgs.findTrackersNotNull())
            trackerTxt.addItem(g.getSimNumber());      
    }
    
    private void loadVehicules(){
        vehiculeTxt.removeAllItems();
        for(Vehicule v : vgs.findVehicules())
            vehiculeTxt.addItem(v.getMatricule());      
    }
    
