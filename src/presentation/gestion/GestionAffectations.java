    private void listAffectationsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listAffectationsMouseClicked
        id = model.getValueAt(listAffectations.getSelectedRow(), 0).toString();
        VehiculeGPSTracker vg = vgs.findById(Integer.parseInt(id));
        if(vg.getDateFin() != null)
            JOptionPane.showMessageDialog(this, "Impossible de désaffecter ce tracker.");
    }//GEN-LAST:event_listAffectationsMouseClicked

    private void affecterBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_affecterBtnMouseClicked
        Vehicule vehicule = vs.findByMatricule(vehiculeTxt.getSelectedItem().toString());
        GPSTracker tracker = gs.findBySimNumber(trackerTxt.getSelectedItem().toString()) ;
        Date date = dateTxt.getDate();
        if(!date.equals("")){
            if(vgs.create(new VehiculeGPSTracker(date, null, vehicule, tracker))){
                load();
                loadTrackers();
                loadVehicules();  
            }
        }else
            JOptionPane.showMessageDialog(this, "Tous les champs sont obligatoires.");
    }//GEN-LAST:event_affecterBtnMouseClicked

    private void desaffecterBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_desaffecterBtnMouseClicked
        if(id != null){           
            int choix = JOptionPane.showConfirmDialog(this, "Vous étes sur?");
            VehiculeGPSTracker vg = vgs.findById(Integer.parseInt(id));
            vg.setDateFin(new Date());
            if(choix == 0){
                if(vgs.updateDateFin(vg)){
                    id = null;
                    load();
                    loadTrackers();
                    loadVehicules();  
                }
            }
        }else
            JOptionPane.showMessageDialog(this, "Veuillez choisir un ligne.");
    }//GEN-LAST:event_desaffecterBtnMouseClicked

