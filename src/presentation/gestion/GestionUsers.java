    private void addBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addBtnMouseClicked
        String nom = nomTxt.getText();
        String prenom = prenomTxt.getText();
        String login = loginTxt.getText();
        String email = emailTxt.getText();
        String password = Hash.encrypt(mdpTxt.getText());
        if(!(nom.equals("") || prenom.equals("") || login.equals("") || email.equals("") || password.equals(""))){
            if(us.create(new User(nom, prenom, login, email, password)))
                load();
            else
                JOptionPane.showMessageDialog(this, "Utilisateur existe déja.");
            
        }else
            JOptionPane.showMessageDialog(this, "Tous les champs sont obligatoires.");
        
    }//GEN-LAST:event_addBtnMouseClicked

    private void deleteBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteBtnMouseClicked
        if(id != 0){
            int choix = JOptionPane.showConfirmDialog(this, "Vous étes sur?");
            if(choix == 0){
                if(us.delete(us.findById(id))){
                    nomTxt.setText("");
                    prenomTxt.setText("");
                    loginTxt.setText("");
                    emailTxt.setText("");
                    mdpTxt.setVisible(true);
                    id = 0;
                    load();
                }         
            } 
        }else
            JOptionPane.showMessageDialog(this, "Veuillez choisir un utilisateur.");
    }//GEN-LAST:event_deleteBtnMouseClicked

    private void listUsersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listUsersMouseClicked
        id = Integer.parseInt(model.getValueAt(listUsers.getSelectedRow(), 0).toString());
        nomTxt.setText(model.getValueAt(listUsers.getSelectedRow(), 1).toString());
        prenomTxt.setText(model.getValueAt(listUsers.getSelectedRow(), 2).toString());
        loginTxt.setText(model.getValueAt(listUsers.getSelectedRow(), 3).toString());
        emailTxt.setText(model.getValueAt(listUsers.getSelectedRow(), 4).toString());
        mdpTxt.setVisible(false);
    }//GEN-LAST:event_listUsersMouseClicked

    private void updateBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateBtnMouseClicked
        if(id != 0){
            int choix = JOptionPane.showConfirmDialog(this, "Vous étes sur?");
            String nom = nomTxt.getText();
            String prenom = prenomTxt.getText();
            String loginT = loginTxt.getText();
            String email = emailTxt.getText();
            User user = us.findById(id);
            user.setNom(nom);
            user.setPrenom(prenom);
            user.setLogin(loginT);
            user.setEmail(email);
            if(choix == 0){
                if(us.update(user)){
                    nomTxt.setText("");
                    prenomTxt.setText("");
                    loginTxt.setText("");
                    emailTxt.setText("");
                    mdpTxt.setVisible(true);
                    id = 0;
                    load();
                }         
            } 
        }else
            JOptionPane.showMessageDialog(this, "Veuillez choisir un utilisateur.");
    }//GEN-LAST:event_updateBtnMouseClicked

