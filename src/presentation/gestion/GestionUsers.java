    private void load() {
        model.setRowCount(0);
        for(User u : us.findAll()){
            model.addRow(new Object []{
                u.getId(),
                u.getNom(),
                u.getPrenom(),
                u.getLogin(),
                u.getEmail()
            });
        }    
    }
    
