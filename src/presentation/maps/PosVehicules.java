    private void loadMap(List<Position> positions) {
        GeoPosition geoPosition[];
        Set<MyWaypoint> waypoints = new HashSet<>();
        List<MyWaypoint> points = new ArrayList();
       
        TileFactoryInfo info = new OSMTileFactoryInfo();
        DefaultTileFactory tileFactory = new DefaultTileFactory(info);

        File cacheDir = new File(System.getProperty("user.home") + File.separator + ".jxmapviewer2");
        tileFactory.setLocalCache(new FileBasedLocalCache(cacheDir, false));

        mapViewer.setTileFactory(tileFactory);

        if(positions.size() > 0){
           geoPosition = new GeoPosition[positions.size()];
           for(int i=0; i<positions.size(); i++)
            geoPosition[i] = new GeoPosition(positions.get(i).getLongitude(),  positions.get(i).getLatitude());   
             
            mapViewer.setZoom(15);
            mapViewer.setAddressLocation(geoPosition[0]);

            MouseInputListener mia = new PanMouseInputListener(mapViewer);
            mapViewer.addMouseListener(mia);
            mapViewer.addMouseMotionListener(mia);
            mapViewer.addMouseListener(new CenterMapListener(mapViewer));
            mapViewer.addMouseWheelListener(new ZoomMouseWheelListenerCenter(mapViewer));
            mapViewer.addKeyListener(new PanKeyListener(mapViewer));

            for(int i=0; i<positions.size(); i++)
                points.add(new MyWaypoint((i+1) +"", Color.RED, geoPosition[i]));
            waypoints = new HashSet<>(points);

            WaypointPainter<MyWaypoint> waypointPainter = new WaypointPainter<>();
            waypointPainter.setWaypoints(waypoints);
            waypointPainter.setRenderer(new FancyWaypointRenderer());
            
            mapViewer.setOverlayPainter(waypointPainter);
            
        }else
            JOptionPane.showMessageDialog(this, "Aucune Position trouvée.");
        
    }

