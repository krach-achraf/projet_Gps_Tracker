    private void rechercherBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_rechercherBtnMouseClicked
        Vehicule vehicule = vs.findByMatricule(vehiculeTxt.getSelectedItem().toString());
        Date dateD = dateDebutTxt.getDate();
        Date dateF = dateFinTxt.getDate();
        if(!(dateD.equals("") || dateF.equals(""))){
            List<Position> positions = ps.findPositionsBetweenDates(vehicule, dateD, dateF);
            loadMap(positions);
        }else
            JOptionPane.showMessageDialog(this, "Tous les champs sont obligatoires.");
    }//GEN-LAST:event_rechercherBtnMouseClicked

